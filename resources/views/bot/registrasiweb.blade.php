1. Siapkan berkas yang diperlukan untuk registrasi, pastikan Anda sudah terdaftar sebagai anggota kelompok tani (untuk petani).
2. Buka situs resmi SIPUKTAN (link)
3. Klik “Daftar Sekarang!”
4. Isikan data diri atau data kios resmi dengan sesuai dan unggah foto KTP.
5. Klik tombol “Daftar”
6. Silakan tunggu verifikasi untuk registrasi akun.
Untuk mengecek verifikasi akun Anda, silakan mencoba login secara berkala. Apabila terdapat kesulitan dalam registrasi, dimohon untuk menghubungi pengurus kelompok tani masing-masing.
